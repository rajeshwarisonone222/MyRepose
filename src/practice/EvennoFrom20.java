package practice;

public class EvennoFrom20 
{

	public static void main(String[] args) 
	{
		int i=20;
		System.out.println("============List of even no from 1 to 20=============: "+i);
		for(int j=1;j<=i;j++)
		{
			if(j%2==0)
			{
				System.out.println(" "+j);
			}
			
		}
		System.out.println("========List of odd no from 1 to 20==============: "+20);
		for(int k=1;k<=i;k++)
		{
			if(k%2==0)
			{
				System.out.println(" "+k);
			}
		}

	}

}
